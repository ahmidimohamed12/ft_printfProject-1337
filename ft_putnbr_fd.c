#include "ft_printf.h"
void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		return (ft_putstr("-2147483648"));
	if (n < 0)
	{
		n = -n;
		ft_putchar('-');
	}
	if (n > 9)
	{
		ft_putnbr_fd((n / 10), fd);
		ft_putchar((n % 10 + '0'));
	}
	else
	{
		ft_putchar((n + '0'));
	}
}
